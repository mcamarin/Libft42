#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*destino;
	char	*fonte;

	i = 0;
	destino = (char *)dest;
	fonte = (char *)src;
	while (i < n)
	{
		destino[i] = fonte[i];
		i++;
	}
	return (dest);
}
